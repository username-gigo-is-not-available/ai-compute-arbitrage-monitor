{% macro round_gpu_mb(col) %}
(
    SELECT t
    FROM (VALUES
        (2048),(3072),(4096),(5120),(6144),(8192),(10240),(11264),(12288),
        (16384),(20480),(24576),(32768),(40960),(49152),(65536),(81920),(98304),(143360),(184320)
    ) tiers(t)
    ORDER BY ABS(t - {{ col }}) LIMIT 1
)
{% endmacro %}